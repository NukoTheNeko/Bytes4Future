console.log("Perimeter");
function squarePerim(side)
{
    return side * 4;
}

console.log(squarePerim(3));
console.log(squarePerim(100));
console.log(squarePerim(4));
console.log(squarePerim(10));

console.log("Area");
function squareArea(side)
{
    return side ** 2;
}

console.log(squareArea(3));
console.log(squareArea(100));
console.log(squareArea(4));
console.log(squareArea(10));

console.log("Volume");
